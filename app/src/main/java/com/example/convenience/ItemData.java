package com.example.convenience;

public class ItemData {
    String pid;
    String pname;
    String pprice;
    String pimage1;
    String pdetail;
    String prdate;

    public ItemData(String pid, String pname, String pprice, String pimage1, String pdetail, String prdate) {
        this.pid = pid;
        this.pname = pname;
        this.pprice = pprice;
        this.pimage1 = pimage1;
        this.pdetail = pdetail;
        this.prdate = prdate;
    }
}
